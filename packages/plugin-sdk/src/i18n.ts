// ── Zustand store ────────────────────────────────────────
interface I18nState {
  locale: Locale;
  setLocale: (locale: Locale) => void;
}

export const useI18nStore = create<I18nState>()((set) => ({
  locale: 'fr', // Default to French
  setLocale: (locale) => set({ locale }),
}));

// ── Hook: useTranslation ─────────────────────────────────
export function useTranslation() {
  const locale = useI18nStore((s) => s.locale);
  const dict = translations[locale];

  const t = (key: string, params?: Record<string, string | number>): string => {
    let text = dict[key] ?? translations.en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  };

  /** Translate an exercise name (English → French if locale is fr).
   * If the DB column name_fr is provided it takes priority over the dict. */
  const tExercise = (name: string, nameFr?: string | null): string => {
    if (locale === 'fr') {
      if (nameFr) return nameFr;
      if (exerciseNames[name]) return exerciseNames[name];
    }
    return name;
  };

  /** Translate a muscle group key */
  const tMuscle = (key: string): string => {
    return dict[`muscle.${key}`] ?? translations.en[`muscle.${key}`] ?? key;
  };

  /** Translate an exercise category */
  const tCategory = (key: string): string => {
    return dict[`exercise.category.${key}`] ?? translations.en[`exercise.category.${key}`] ?? key;
  };

  /** Translate a meal type */
  const tMeal = (key: string): string => {
    return dict[`meal.${key}`] ?? translations.en[`meal.${key}`] ?? key;
  };

  return { t, tExercise, tMuscle, tCategory, tMeal, locale };
}
